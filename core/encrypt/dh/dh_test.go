func Test_DH64(t *testing.T) {
	di := &dh.DH{
		P: 0xffffffffffffffc5,
		G: 5,
	}

	for i := 0; i < len(data); i += 2 {
		data1 := data[i]
		data2 := data[i+1]

		publicKey1 := di.PublicKeyOf(data1.PrivateKey)
		assert.Equal(t, publicKey1, data1.PublicKey)

		publicKey2 := di.PublicKeyOf(data2.PrivateKey)
		assert.Equal(t, publicKey2, data2.PublicKey)

		secret1 := di.LocalKey(data1.PrivateKey, data2.PublicKey)
		assert.Equal(t, secret1, data1.Secret)

		secret2 := di.LocalKey(data2.PrivateKey, data1.PublicKey)
		assert.Equal(t, secret2, data2.Secret)
	}
}

